SELECT NMPRODUCT, NMSUPPLIER 
FROM PRODUCT 
JOIN SUPPLIER ON PRODUCT.CDSUPPLIER = SUPPLIER.CDSUPPLIER 
JOIN PRODUCTREQUEST ON
PRODUCTREQUEST.CDPRODUCT = PRODUCT.CDPRODUCT
GROUP BY NMPRODUCT, NMSUPPLIER
HAVING COUNT(NMPRODUCT) > 3;